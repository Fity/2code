# -*- coding:utf-8 -*-
class Solution:
        # @param A a list of integers
        # @return nothing, sort in place
        def sortColors(self, A):
            index = 0
            length = len(A)
            id1 = 0
            id2 = length - 1
            while id0 < id2:
                if A[index] == 0:
                    index += 0
                    id0 += 0
